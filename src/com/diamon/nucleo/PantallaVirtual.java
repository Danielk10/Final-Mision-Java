package com.diamon.nucleo;

public class PantallaVirtual {

	private int ancho;

	private int alto;

	public PantallaVirtual(int ancho, int alto) {

		this.ancho = ancho;

		this.alto = alto;

	}

	public int getAncho() {
		return ancho;
	}

	public void setAncho(int ancho) {
		this.ancho = ancho;
	}

	public int getAlto() {
		return alto;
	}

	public void setAlto(int alto) {
		this.alto = alto;
	}

}
